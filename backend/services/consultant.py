from backend.services.yandex_client import YandexClient
from backend.utils.text import clean_text


class ConsultantService:
    SYSTEM = (
        "Ты AI-консультант сервиса «СоздайСвоюПесню» (SongForge). "
        "Помогаешь пользователю создать песню без знания промптов. "
        "Отвечай кратко, дружелюбно, на русском. "
        "Объясняй простым языком. "
        "Модель нот: после входа — 1 пробная генерация (2 превью по 30 сек). "
        "Дальше 1 нота = 1 песня = 2 полных варианта в фонотеку. "
        "Без входа создать песню или текст нельзя. "
        "Пакеты нот: 1 за 199₽, 3 за 499₽, 5 за 799₽, 10 за 1199₽. "
        "Музыка обычно готова за 1–3 минуты. "
        "AI-продюсер — быстрый старт и черновик; Расширенный режим с детальным "
        "брифом (жанр, BPM, вокал, референсы) даёт шанс на более сильный трек. "
        "Подсказывай, как улучшить идею и описание звучания."
    )

    FAQ = {
        "цена": (
            "1 нота = 199₽ — одна генерация, 2 варианта песни в фонотеку. "
            "Пакеты: 3 ноты — 499₽, 5 — 799₽, 10 — 1199₽."
        ),
        "сколько": "Текст и план — около 10–20 секунд. Музыка — обычно 1–3 минуты.",
        "жанр": (
            "В AI-продюсере достаточно идеи — стиль подберёт AI. "
            "Для точного звука включите Расширенный режим: жанр, темп, вокал, референсы."
        ),
        "бесплатн": (
            "После входа — 1 пробная генерация: 2 варианта по 30 сек превью. "
            "Полные треки — за ноты. Без входа создать песню нельзя."
        ),
        "пробн": (
            "1 пробная генерация на аккаунт после регистрации: "
            "2 превью по 30 секунд. Дальше — покупка нот."
        ),
        "нот": (
            "1 нота списывается при создании песни — вы получаете 2 полных варианта "
            "в фонотеку. Пополнить баланс можно в разделе «Пакеты»."
        ),
        "оплат": (
            "Оплата пакетов нот через сайт (пока тестовый режим). "
            "После подключения платёжной системы — карты и СБП."
        ),
        "вход": (
            "Войдите по email на сайте — код придёт на почту "
            "(или покажется на экране, пока почта не настроена)."
        ),
    }

    def __init__(self) -> None:
        self._yandex = YandexClient()

    def reply(self, message: str, context: str = "") -> str:
        message = message.strip()
        if not message:
            return "Напишите, чем помочь — идея песни, жанр, цены или как работает сервис."

        user_text = message
        if context.strip():
            user_text = f"Контекст: {context.strip()}\n\nВопрос: {message}"

        try:
            answer = self._yandex.complete(
                self.SYSTEM,
                user_text,
                max_tokens=350,
                temperature=0.6,
            )
            return clean_text(answer)
        except Exception:
            low = message.lower()
            for key, value in self.FAQ.items():
                if key in low:
                    return value
            return (
                "Опишите идею песни своими словами — AI-продюсер подберёт стиль. "
                "После входа доступна 1 пробная генерация; дальше — ноты. "
                "Спрашивайте про цены, режимы или как получить лучший звук!"
            )